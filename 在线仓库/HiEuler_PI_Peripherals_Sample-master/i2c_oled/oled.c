static void oled_usage(char *prg_name)
{
    printf("usage : %s <device_node> <index> \n", prg_name);
    printf("device_node : OLED device node, e.g., /dev/oled-1\n");
    printf("index:\n");
    printf("    (0) clear screen\n");
    printf("    (1) display image\n");
    printf("    (2) power on display\n");
    printf("    (3) power off display\n");
}

int main(int argc,char*argv[])
{
    if (argc != 3 || !strncmp(argv[1], "-h", 2)) {
        oled_usage(argv[0]);
        return -1;
    }

    const char* oled_dev = argv[1];
    int index = atoi(argv[2]);

    int fd = open(oled_dev, O_RDWR);
    if (fd < 0) {
        perror("open failed");
        return -1;
    }

    uint8_t data;
    int ret = 0;

    switch (index) {
        case 0: // 清屏
            data =  0;
            ret = ioctl(fd, OLED_CMD_CLEAR, &data);
            break;

        case 1: // 显示图像
            ret = ioctl(fd, OLED_CMD_FRAME, gImage_ebaina);
            break;

        case 2: // 打开 OLED 电源
            data = 1;
            ret = ioctl(fd, OLED_CMD_POWER, &data);
            break;

        case 3: // 关闭 OLED 电源
            data = 0;
            ret = ioctl(fd, OLED_CMD_POWER, &data);
            break;

        default:
            oled_usage(argv[0]);
            close(fd);
            return -1;
    }

    if (ret == -1) {
        perror("ioctl failed");
    }

    close(fd);
    return 0;
}